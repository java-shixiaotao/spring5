package com.mouds.autowire;

public class Dept {
    private String dname;

    public Dept() {
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    @Override
    public String toString() {
        return "Dept{" +
                "dname='" + dname + '\'' +
                '}';
    }
}
